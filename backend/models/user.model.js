import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    profilePicture: {
        type: String,
        default: "https://www.pngmart.com/files/23/Profile-PNG-Photo.png",
    },
}, { timestamps: true })

export const User = mongoose.model("User", userSchema)